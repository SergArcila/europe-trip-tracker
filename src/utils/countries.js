export function findCountry(name) {
  if (!name) return null;
  const lower = name.toLowerCase();
  return ALL_COUNTRIES.find(c =>
    c.name.toLowerCase() === lower || c.code.toLowerCase() === lower
  );
}

export function countryFlag(name) {
  return findCountry(name)?.flag || '🏳️';
}
